export const morningGreet = (name: string): string => {
  return `Good morning, ${name}!`;
}
// This function takes a name as input and returns a greeting string for the morning.
// It can be used to greet someone in the morning with a personalized message.
// Example usage:
// console.log(morningGreet("Alice")); // Output: Good morning, Alice!
// This function is useful for applications that need to send morning greetings,
// such as a morning reminder app or a daily greeting service.
